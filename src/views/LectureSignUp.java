    private void signUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpBtnActionPerformed
        String name = nameTxt.getText();
        int lectureId = 0;
        try{
        lectureId = Integer.parseInt(lectureIdTxt.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Lecture Id :Please enter number value");
        }
        String email = emaiTxt.getText();
        String password = passwordTxt.getText();
        String lectureLevel = lectureLevelTxt.getText();
        
        Lecture lecture  = new Lecture(lectureId,name,password,lectureLevel );
        if(lectureId !=0){
            try{
                int res = LectureController.addLecture(lecture);
                if(res>0){
                    JOptionPane.showMessageDialog(this,"Added Success");
                    java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                    new AdminInterface().setVisible(true);
                        }
                    });
                }
            }catch(ClassNotFoundException ex){
                JOptionPane.showMessageDialog(this, "No Driver found");
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_signUpBtnActionPerformed

