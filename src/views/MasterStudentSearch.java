    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        int userName = 0;
        try{
            userName =  Integer.parseInt(userNameTxt.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invaild value","error",JOptionPane.ERROR_MESSAGE);
        }
        if(userName != 0){
            try{
                MasterStudent mstu = MasterStudentController.searchStudent(userName);
                MasterStudent mstu1 = MasterStudentController.searchAddress(userName);
                if(mstu.getStudentId()!=0){
                    fullNameTxt.setText(mstu.getFullName());
                    nameWithIntialsText.setText(mstu.getNameWithInitials());
                    studentId.setText(Integer.toString(mstu.getStudentId()));
                    emailTxt.setText(mstu.getEmail());
                    yearTxt.setText(Integer.toString(mstu.getYear()));
                    addressNoTxt.setText(mstu1.getAddressNo());
                    addressRdTxt.setText(mstu1.getAddressRd());
                    addressTownTxt.setText(mstu1.getAddressCity());
                    passwordTxt.setText(mstu.getPassword());
                }
                else{
                     JOptionPane.showMessageDialog(this, "No such Master Student");
                    fullNameTxt.setText("");
                    nameWithIntialsText.setText("");
                    studentId.setText("");
                    emailTxt.setText("");
                    yearTxt.setText("");
                    passwordTxt.setText("");
                    addressNoTxt.setText("");
                    addressRdTxt.setText("");
                    addressTownTxt.setText("");
                }
            }catch (ClassNotFoundException ex) {
                Logger.getLogger(MasterStudentSearch.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(MasterStudentSearch.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

