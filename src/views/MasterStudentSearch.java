    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        int stuId = Integer.parseInt(userNameTxt.getText());
        String fullName = fullNameTxt.getText();
        String nameWithInitials = nameWithIntialsText.getText();
        String email = emailTxt.getText();
        String password = passwordTxt.getText();
        int year = Integer.parseInt(yearTxt.getText());
        String addressNo = addressNoTxt.getText();
        String addressRd = addressRdTxt.getText();
        String addressCity = addressTownTxt.getText();
        MasterStudent mstu = new MasterStudent();
        mstu.setStudentId(stuId);
        mstu.setFullName(fullName);
        mstu.setNameWithInitials(nameWithInitials);
        mstu.setEmail(email);
        mstu.setPassword(password);
        mstu.setYear(year);
        mstu.setAddressNo(addressNo);
        mstu.setAddressRd(addressRd);
        mstu.setAddressCity(addressCity);
        int res = 0;
        int res2 = 0;
        try{
            res = MasterStudentController.updateMasterStudent(mstu);
            res2 = MasterStudentController.updateAddress(mstu);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MasterStudentSearch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(MasterStudentSearch.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(res>0 && res2>0){
            JOptionPane.showMessageDialog(this, "Update Success");
        }else{
            JOptionPane.showMessageDialog(this, "Update Fail");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

